    public void fillTable()
    {
        try {
            DefaultTableModel model = new DefaultTableModel();
            jTableEmployee.setModel(model);
            model.addColumn("Documento");
            model.addColumn("Nombre completo");
            model.addColumn("Tipo de empleado");
            
            String [] rows = new String [3];
            List<Employee> employees = employeeControllers.findAll();
            for (Employee e: employees)
            {
                rows[0] = String.valueOf(e.getDocument());
                rows[1] = e.getFullname();
                rows[2] = e.getEmployeeType().getDescript();
                model.addRow(rows);
            }
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }
    
    public void fillComboTypes()
    {
        try { 
                  List<EmployeeType> types = employeeTypeControllers.findALL();
                  DefaultComboBoxModel  model = new DefaultComboBoxModel();
                  jComboBoxType.setModel(model);
                  model.addAll(types);
                  
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }
    
