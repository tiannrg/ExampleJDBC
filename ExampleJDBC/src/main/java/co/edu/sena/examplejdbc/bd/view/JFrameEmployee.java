    private void jTableEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEmployeeMouseClicked
            int rowSelected= jTableEmployee.getSelectedRow();
            if (rowSelected != -1)
            {
                long documentSelect = Long.parseLong(jTableEmployee.getValueAt(rowSelected,0).toString());
                
                try {
                    Employee employee = new EmployeeControllers().findById(documentSelect);
                    jTextFieldDocument.setText(String.valueOf(documentSelect));
                    jTextFieldFullName.setText(employee.getFullname());
                    jTextFieldAdress.setText(employee.getAddress());
                    jTextFieldPhone.setText(employee.getPhone());
                    jComboBoxType.setSelectedItem(employee.getEmployeeType());
                } catch (Exception e) {
                    MessageUtils.showErrorMessage(e.getMessage());
                }
               
            }
    }//GEN-LAST:event_jTableEmployeeMouseClicked

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
            try {
                 int option = JOptionPane.showConfirmDialog(rootPane,"Esta seguro de eliminar el registro?",
                    "Confirmacion", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION)
            {
                employeeControllers.delete(Long.parseLong(jTextFieldDocument.getText()));
                MessageUtils.showInfoMessage("Empleado eliminado exitosamente");
                fillTable();
            }
            clean();
        } catch (Exception e) {
            MessageUtils.showErrorMessage(e.getMessage());
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

